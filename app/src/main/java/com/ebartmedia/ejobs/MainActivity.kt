    private fun fetchData() {

        compositeDisposable?.add(jsonApi.categories
            .subscribeOn(Schedulers.io())
            .observeOn(AndroidSchedulers.mainThread())
            .subscribe( {

                    categories -> displayCategories(categories)


                  Log.d("categories", "categories" + categories)

            }))
    }

    private fun displayCategories( categories: List<Categories>?) {

        //  categories.toString()

        val category = categories!![1]?.category_name

        Log.d("categories", "categories" + category.toString())


         val adapter = CategoriesAdapter(this, categories!!)

        recycler_categories.adapter = adapter




    }
